									<div class="detail">
										<p>Untuk menuju ke lokasi pemandian air panas Cangar ini masih belum ada kendaraan umum atau angkutan umum yang dapat mengantar dan menjemput kalian ke tempat tersebut. Sehingga untuk ke tempat pemandian air panas ini otomatis kalian harus membawa mobil atau motor pribadi, mungkin juga kalian dapat menyewa mobil dari kota Batu maupun dari kota Malang untuk menuju tempat tersebut.</p>
										<p>Jarak dari pusat kota Batu Malang kurang lebih sekitar 20 km saja, waktu yang dibutuhkan dalam menempuh jarak tersebut hingga sampai ke tempat pemandian air panas tersebut kurang lebih membutuhkan waktu sekitar 1 jam saja. Area pemandian air panas tersebut masih berada dalam satu area dengan wilayah Taman Hutan Rakyat Raden Suryo. Tempatnya merupakan perbatasan dengan Kabupaten Mojokerto.</p>
										<p>Dalam perjalanan menuju ke area pemandian air panas, kalian dapat melihat ladang dan kebun penduduk yang hijau. Yang nuansa pegunungannya masih terasa alami dan asri. Tetapi kalian harus hati-hati dikarenakan jalan untuk menuju tempat wisata ini begitu banyak jalanan yang berkelok, menanjak, menikung tajam, sering juga terdapat cerita-cerita mistis, horror dan angker. Terutama ketika kendaraan yang kalian gunakan tak kuat untuk menanjak jalan tersebut. Di daerah jalan itu yang menuju ke are pemandian air panas Cangar terdapat papan peringatan untuk hal tersebut.</p>
										<p>Tetapi kalian tidak usah khawatir tentang keadaan jalannya, karena semua jalan menuju tempat pemandian air panas sudah beraspal semua. Ada rute perjalanan untuk menuju tempat wisata pemandian air panas Cangar ini, banyak yang menyarankan untuk melalui rute alun-alun kota Batu Malang menuju ke arah barat lalu kemudian terdapat perempatan dan belok ke kanan. Obyek wisata pemandian air panas Cangar tersebut, satu jalur ke arah dengan Coban Talun dan Selecta Malang. Sehingga kalian tak perlu khawatir untuk tersesat, karena banyak wisatawan yang berada di rute tersebut. Kalian akan menikmati jalanan berkelak – kelok dengan disertai pemandangan yang indah dan kalian juga dapat merasakan makanan khas area tersebut, yaitu tape ketan. Makanan khas tape ketan tersebut sudah terkenal, disebabkan berjualannya dilaksanakan dengan cara turun menurun selama puluhan tahun, jadi dari nenek moyang hingga cicit – cicitnya didaerah Cangar ini.</p>
