									<strong class="header-box" style="text-align: center;">LETAK GEOGRAFIS</strong>
									<div class="detail">
										<p>Panorama Petung Sewu merupakan wana wisata terbaru di Tahura Raden Soerjo. Lokasinya terletak di desa Pacet Kecamatan Pacet Kabupaten Mojokerto. Teman-teman yang berkunjung kesini akan diberi keindahan pemandangan alam Tahura berupa spot-spot foto berlatar belakang pegunungan dan air terjun. Pada lokasi ini juga dapat ditemui hutan bambu alami serta aliran sungai yang relatif jernih dengan ketinggian 1.500 mdpl.</p>
									</div>
								</div>
								<div class="col-md-12" style="margin-top: 20px">
									<div class="detail">
										<p>Kawasan Taman Hutan Raya Raden Soerjo di wilayah Gunung Arjuno memang menyimpan keindahan yang sangat menarik. Salah satu destinasi wisata yang sekarang mulai naik daun adalah Panorama Petung Sewu. Destinasi ini menyuguhkan keindahan panorama lereng Gunung Arjuno dan juga hijaunya hutan liar ini berada di daerah Pacet. Penasaran dengan keindahannya? Yuk simak ulasannya di bawah ini.</p>
										<p>Destinasi wisata berupa spot selfie seperti Panorama Petung Sewu ini mungkin sudah sangat banyak. Tapi kebanyakan, pengunjung diharuskan membayar tiket untuk masuk ke setiap spotnya. Namun, hal tersebut tidak akan kita temukan di tempat wisata ini, karena biaya masuknya hanya Rp10.000 dan pengunjung bebas menikmati spot mana saja.</p>
										<p>Berjalan memasuki area tempat wisata, kita akan menemukan sebuah spanduk yang berisi keterangan spot selfie apa saja yang ada di tempat ini. Dengan begini, kita akan lebih mudah mengetahui mana spot yang akan dicoba.</p>
										<p>Masuk ke area selfie kita akan disuguhkan lebih dari 5 spot untuk berfoto. Saat liburan atau akhir pekan, tempat selfie ini akan sangat dipadati pengunjung yang datang. Oleh karena itu, kita diharuskan bergantian dengan pengunjung lain dan diberikan batasan waktu maksimal selama 10 menit per orang untuk berfoto.</p>
										<p>Tapi kalau kita datang ke Panorama Petung Sewu ini di hari biasa, tentunya akan lebih leluasa. Kita bisa dengan bebas foto berlama-lama bersama teman-teman kita tanpa dibatasi waktu. Nah, yang perlu diingat, untuk berfoto di salah satu spot foto berupa papan kayu segi enam, hanya boleh dinaiki 3 sampai 4 orang secara bersamaan.</p>
										<p>Selain papan kayu segi enam, tepat di sampingnya kita akan menemukan spot foto lain yang ada hiasan bunga-bunga dan seperti rumah kecil. Masih menghadap ke perbukitan hijau di sekitar sana, spot ini tidak kalah ramai diminati pengunjung untuk berfoto.</p>
										<p>Selain taman untuk selfie, tempat wisata ini ternyata juga menyajikan pemandangan lain yang sangat mempesona. Seperti dinding bukit di seberang yang menyajikan pemandangan aliran sungai yang mirip dengan air terjun.</p>
										<p>Di sini juga terdapat beberapa fasilitas yang bisa digunakan pengunjung. Seperti kamar mandi dan mushola yang masih tergolong baru menurut pengelola. Kemudian ada hammock yang berlokasi di camping area. Pengunjung bisa menggunakannya untuk sekadar berfoto atau besantai.</p>
