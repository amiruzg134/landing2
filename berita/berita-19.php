                            <h3 class="blog-details__title">Penertiban Jalur Cangar-Pacet</i></h3>
                            <p class="blog-details__text">
                                Jalan provinsi yang membelah Kawasan Pelestarian Alam (KPA) Tahura Raden Soerjo sepanjang Cangar hingga Pacet Mojokerto adalah merupakan <strong>Blok Khusus</strong> yang ditetapkan seluas 12 hektar atau 0,04% dari Luas Tahura Raden Soerjo. Jalan ini kebanyakan digunakan oleh masyarakat sekitar kawasan untuk menjangkau wilayah Kabupaten Mojokerto dan sebaliknya untuk ke Kota Batu. Namun jalan itu juga digunakan masyarakat luas sebagai jalur alternatif menuju ke dan dari Surabaya-Malang, sebelum dibangunnya tol Malang-Pandaan apabila hendak berwisata ke Malang Raya terutama ke Kota Batu.  
                            </p>
                            <p class="blog-details__text">
                                Jalan Cangar-Pacet, walaupun merupakan jalan provinsi namun karena berada dalam kawasan hutan konservasi maka masyarakat luas harus mengetahui bahwa <strong>jalan ini bukan seperti halnya jalan raya biasa</strong>. Status kanan kiri jalan ini adalah hutan konservasi dimana dijelaskan dalam peraturan perundangan, kawasan konservasi tidak dapat dimanfaatkan kecuali telah diubah statusnya sebagai blok/zona pemanfaatan. Sehingga <strong>tidak diperbolehkan untuk tempat berjualan</strong> baik yang mobile, semi permanen, maupun permanen. Bangunan yang diperkenankan untuk didirikan hanyalah untuk sarana perlindungan dan pengamanan kawasan. Bahkan tempat ibadah-pun tidak diperkenankan untuk didirikan, kecuali apabila status kawasan telah diubah menjadi blok pemanfaatan.  
                            </p>
                            <p class="blog-details__text">
                               Masyarakat umumnya masih kurang memahami mengapa kawasan konservasi sebegitu ketatnya, tidak dapat dimanfaatkan semaunya, padahal kawasan hutan konservasi kebanyakan mempunyai daya tarik untuk dikembangkan dan bernilai jual tinggi, yang dapat digunakan untuk meningkatkan kesejahteraan masyarakat sekitar dengan berjualan dan lain-lain. Hal tersebut tidak dapat dilakukan karena hutan konservasi adalah <strong>benteng perlindungan sistem penyangga kehidupan dan perlindungan keanekaragaman hayati. Tanpa hutan konservasi, air, udara bersih dan keanekaragaman hayati</strong> yang menjadi penyangga kehidupan manusia tidak akan tersedia. Kawasan hutan konservasi-lah yang menjadi tandon raksasa air tawar di daratan yang hanya tersedia 3% dari jumlah air yang ada di bumi ini, yang digunakan untuk memenuhi kebutuhan rumah tangga, pertanian, energi dan industri. Kawasan hutan konservasi-lah mempertahankan agar berbagai jenis flora tetap ada sebagai penghasil oksigen dan tempat hidup berbagai macam satwa. Satwa yang ada bukannya tanpa manfaat, tapi keberadaannya adalah sebagai pengendali rantai makanan. 
                            </p>
                            <p class="blog-details__text">
                               Blok khusus jalan Cangar-Pacet dapat ditempuh dalam waktu kurang lebih 30 menit. Namun waktu tersebut tidak akan terasa lama karena pengguna jalan akan disuguhi dengan pemandangan indah dan suasana hutan pegunungan yang segar. Hal tersebut membuat pengguna jalan tertarik untuk berhenti sejenak, berswafoto sambil menikmati pemandangan alam. Namun yang harus diperhatikan pengguna jalan adalah jangan sampai kegiatannya mengganggu penguna jalan yang lain, bahkan menimbulkan kecelakaan. Penjual makanan pun memberikan dampak buruk terhadap kawasan, karena selain menyebabkan banyak pengguna jalan berhenti tidak pada tempatnya, juga menimbulkan sampah dan mengganggu kehidupan satwa liar yang ada didalam kawasan. 
                            </p>
                            <p class="blog-details__text">
                               Tempat yang sering digunakan untuk pengguna jalan dan penjual makanan adalah sepanjang 2 jembatan dekat Cangar, Blok Lemahbang dan belokan tajam Gajah Mungkur. Jembatan yang ada hanya cukup untuk dilalui 2 mobil yang berpapasan, namun sering digunakan oleh pengguna jalan untuk parkir dan berswafoto serta makan sambil menikmati pemandangan. Demikian juga sekitar Blok Lemahbang yang mempunyai area lapang serta belokan Gajah Mungkur yang sempit dan sulit, namun masih ada saja yang memarkir kendaraan seenaknya. Hal tersebut dapat membahayakan pengguna jalan yang lain. Selain mengganggu lalu lintas, perilaku pengguna jalan ini juga menimbulkan sampah dan mengganggu satwa. Sehingga pihak Tahura Raden Soerjo menempatkan rambu-rambu dan petugas untuk menertibkan serta membubarkan pengguna jalan agar tidak parkir disepanjang jembatan dan sepanjang belokan Gajah Mungkur. <i>@by hildalho </i>
