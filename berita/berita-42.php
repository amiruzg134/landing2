                                Pemerintah Provinsi Jawa Timur menggelar acara tahunan yaitu East Java Tourism Award 2023. Acara ini diselenggarakan di Taman Candra Wilwaktika Sumber Gedang, Kecamatan Pandaan, Kabupaten Pasuruan Selasa (31/10/2023).
