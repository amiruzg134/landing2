                                Dalam rangka memperingati Hari Bhakti Rimbawan ke-38 tanggal 16 Maret 2021, Tahura Raden Soerjo melaksanakan kegiatan penanaman yang merupakan upaya pengayaan di dalam kawasan. Kawasan yang ditanami adalah yang mengalami degradasi akibat kebakaran hutan dan perambahan. Jumlah yang ditanam sebanyak 1.142 batang di 6 blok yaitu Blok Joko Semujung Desa Tawangrejo Jatirejo Kabupaten Mojokerto, Blok Pos 2 Jalur Pendakian Lawang Kabupaten Malang, Blok Coban Supit Urang Desa Madiredo Kecamatan Pujon Kabupaten Malang, Blok Bon 3 Desa Sumberbrantas Kecamatan Bumiaji Kota Batu, Blok Garotan Kelurahan Ledug Kecamatan Prigen Kabupaten Pasuruan, dan Blok Sintok Desa Panglungan Kecamatan Wonosalam Kabupaten Jombang. Jumlah yang ditanam tidaklah banyak dan hanya dibeberapa tempat saja, namun langkah nyata para rimbawan pada musim penghujan ini diharapkan mampu membantu mempercepat penutupan hutan, karena apabila hanya mengandalkan permudaan alam akan memakan waktu yang sangat lama. 
