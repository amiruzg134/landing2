                            <h3 class="blog-details__title">AKSI PENGAYAAN TANAMAN DAN SABER SAMPAH DALAM KAWASAN SEBAGAI WUJUD BAKTI PARA RIMBAWAN TAHURA RADEN SOERJO</i></h3>
                            <p class="blog-details__text">
                                Hari Bhakti Rimbawan yang selalu diperingati setiap tanggal 16 Maret, merupakan hari yang menjadi tonggak terbentuknya Departemen Kehutanan. Hari awal dimana akhirnya rimbawan dapat berkiprah total dalam mengelola kehutanan serta membangun hutan dan kehutanan sehingga hutan di Indonesia dapat membawa manfaat tidak hanya secara ekologi namun juga dari segi ekonomi bagi masyarakat sekitarnya.   
                            </p>
