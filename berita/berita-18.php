                            <h3 class="blog-details__title">Masa Pandemi Covid-19, Obyek Wisata Alam (OWA) Air Panas Cangar Berbenah</i></h3>
                            <p class="blog-details__text">
                                Sebagai salah satu ikon unggulan Taman Hutan Raya (Tahura) Raden Soerjo, Obyek Wisata Alam (OWA) Air Panas Cangar adalah merupakan sumber air panas yang mempunyai kandungan belerang tinggi yang berasal dari Gunung Welirang. Suhu air yang mencapai 40-an derajat dan kandungan belerang yang tinggi sangat sesuai untuk pengobatan berbagai macam penyakit kulit. Namun Cangar tidak hanya pemandian air panas. Cangar adalah salah satu blok pemanfaatan Tahura Raden Soerjo yang dikelola oleh Resort Pengelolaan Hutan (RPH) 05 Cangar. Cangar walaupun sudah tidak banyak pepohonannya yang mengindikasikan sebagai suatu hutan karena telah dikembangkan sebagai wisata, tetapi statusnya tetap merupakan kawasan hutan konservasi.  
                            </p>
                            <p class="blog-details__text">
                                Cangar yang terkenal sebagai pemandian air panas, ternyata tidak hanya mempunyai potensi air panas. Selayaknya hutan, keanekaragaman hayati di Cangar juga tinggi, sama seperti hutan konservasi kebanyakan, area Cangar ditumbuhi berbagai pohon-pohon endemik seperti Anggrung, Kukrup, Cemara Gunung, Pasang, Genitri, Kesek, Lamer, Nyampoh, berbagai jenis pohon Tutup; dihuni satwa-satwa khas hutan seperti primate (lutung dan monyet ekor panjang), berbagai macam burung, ayam hutan dan mamalia seperti rusa, kijang, babi hutan/celeng, landak, serangga dll. Belum lagi potensi pakis-pakisan, paku-pakuan, lumut, anggrek, jamur, pandan gunung yang mempunyai kekhasan tersendiri.  
                            </p>
                            <p class="blog-details__text">
                               Dalam blok pemanfaatan Cangar juga dapat ditemui obyek wisata alam lainnya selain pemandian air panas, yaitu jogging track yang akan membawa pengunjung melintasi hutan sekunder menuju Gua Jepang. Bagi pecinta anggrek, anggrek di Cangar tidak seperti anggrek hibrida yang berukuran besar, berwarna menarik dan cantik. Anggrek Cangar dapat ditemui sepanjang jogging track, tumbuh di pohon-pohon besar seperti Kukrup, Anggrung dan Pasang dan bunganya kecil-kecil namun cantik. Cangar juga terkenal dengan jenis jamur kremposnya <i>(Brown puffball mushroom)</i>. 
