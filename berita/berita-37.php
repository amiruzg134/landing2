                            <p class="blog-details__text">
                                Tertangkap kamera trap yang dipasang di lereng Welirang, musang ini sedang berlari dan menaiki pohon yang berada dekat camera trap.  Awalnya satwa ini terekam pada camera trap lainnya yang terpasang dekat sungai, sedang melintas. Waktu itu tim pemantauan satwa Tahura bersama-sama Balai Besar KSDA Jawa Timur serta relawan Tahura masih belum dapat memastikan bahwa musang yang melintas dengan cepat itu adalah Martes si musang leher kuning. Namun dari ciri fisik mulai leher hingga kepala yang berwarna kuning yang kontras dengan bagian tubuh lainnya yang berwarna coklat tua, satwa tersebut akhirnya dilaporkan sebagai musang leher kuning. Setelah kemunculannya yang kedua, tim baru dapat dengan jelas mengidentifikasi bahwa memang benar satwa tersebut adalah Martes flavigula si musang leher kuning.
                            </p>
                            <p class="blog-details__text">
                                Tahura Raden Soerjo adalah salah satu kawasan konservasi di Jawa Timur yang dikelola oleh Dinas Kehutanan Provinsi Jawa Timur. Tahura Raden Soerjo mengelola 3 gugusan pegunungan, yaitu Arjuno, Welirang dan Anjasmoro. Dengan terekamnya musang leher kuning di lereng Welirang ini menambah semangat pihak pengelola untuk melakukan pemantauan satwa karena baru kali ini jenis Martes ditemukan sebarannya di Tahura Raden Soerjo.
                            </p>
