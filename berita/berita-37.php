                                Musang ini termasuk jenis omnivora, yang tidak hanya memakan buah-buahan dan nectar, namun juga memangsa satwa kecil. Menurut lembaga konservasi internasional/International Union for Conservation of Nature (IUCN), Martes masih termasuk dalam kategori resiko rendah/least concern. Namun perlu dilakukan pengamatan lebih lanjut agar dapat diketahui populasi dan sebarannya di Tahura selain di Pegunungan Welirang.
