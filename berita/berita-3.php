                            <h3 class="blog-details__title">Mantra Summit Challenge</h3>
                            <p class="blog-details__text">
                                Mantra Summits Challenge 2019 (Mantra Summits 2019)™ adalah perhelatan lomba lari lintas alam ketiga yang diselenggarakan oleh Malang Trail Runners (Mantra Runners). Kegiatan ini diselenggarakan oleh Mantra Runners yang berkerjasama dengan Tahura R. Sorejo sebagai lintasan dan Kaliandra Eco Resort sebagai venue lomba dan start/finish.
                            </p>
                            <p class="blog-details__text">
                                Mantra Runners merupakan komunitas lari lintas alam di Malang yang terbentuk dari beragam latar belakang profesi namun memiliki minat yang sama yakni olah raga lari lintas alam. Perhelatan yang pertama dikenal dengan Arjuna Welirang Ultra (AWU 2016)™ yang diselenggarakan Agustus 2016 mengambil rute start dan finish di kebun teh Wonosari-Lawang. Perhelatan ke 2 sampai ke 4 start finish di Kaliandra Eco Resort. Kategori yang dilombakan adalah 15k, 35k, 55K, 75K dan 116k dengan rute melintasi gunung Arjuna, Kembar 2, Kembar 1 dan Welirang.
                            </p>
                            <p class="blog-details__text">
                                Jumlah Peserta setiap tahun meningkat, pada tahun;
                                <ul>
                                    <li>2016 : 160 Peserta</li>
                                    <li>2017 : 320 Peserta</li>
                                    <li>2018 : 620 Peserta</li>
                                    <li>2019 : 844 Peserta</li>
                                </ul>
                            </p>
                            <p class="blog-details__text">
                                Perlombaan ini didaftarkan ke Asosiasi Lari Trail Indonesia (ALTI) serta organisasi lari lintas alam Asia - Asia Trail Master (ATM)™–pelari akan mendapatkan point apabila mengikuti kegiatan lomba ini. Tahun ini MSC masuk dalam International Trail Run Association (ITRA) dan bila pelari berhasil menamatkan akan mendapat poin untuk berlaga di perhelatan akbar lari lintas alam yang diselenggarakan rutin tiap tahun melintasi 3 negara: Ultra Trail Mont Blanc (UTMB).
                            </p>
                            <p class="blog-details__text">
                                Kegiatan ini akan didukung oleh panitia dan volunteer yang berjumlah kurang lebih 250 orang dari Mantra Runners, Tahura, Kaliandra Eco Resort, Perhutani, dan gabungan dari komunitas lari di Malang dan sekitarnya.
