                            <p class="blog-details__text">
                                Tahura Raden Soerjo sebagai pengelola kawasan pelestarian alam yang meliputi Gunung Arjuno, Gunung Welirang dan Gunung Anjasmoro seluas 27.868,30 hektar, didukung oleh tenaga pengamanan hutan sebanyak 81 orang terdiri dari 4 orang polisi kehutanan dan 77 orang satuan pengamanan hutan. Tenaga pengamanan hutan Tahura ini mempunyai tugas pokok melakukan upaya perlindungan hutan, yaitu mencegah dan membatasi kerusakan hutan, kawasan hutan dan hasil hutan yang disebabkan oleh perbuatan manusia, ternak, kebakaran, daya-daya alam, hama penyakit serta mempertahankan dan menjaga hak-hak negara. Guna meningkatkan kesiapsiagaan tenaga pengamanan hutan dalam pelaksanaan tugasnya, Tahura Raden Soerjo mengadakan latihan kesamaptaan di Obyek Wisata Alam (OWA) Air Panas Cangar pada hari Selasa tanggal 19 Nopember 2019. Latihan kesamaptaan ini merupakan upaya pembinaan yang bertujuan untuk meningkatkan kedisiplinan, kepatuhan serta ketaatan dalam menjalankan tugas berdasarkan komando pimpinan, kesiapan fisik dan mental, dedikasi serta membangun korsa/kekompakan para tenaga pengamanan hutan Tahura.
                            </p>
                            <p class="blog-details__text">
                                Latihan kesamaptaan dibuka dengan apel yang dipimpin oleh Kepala UPT Tahura Raden Soerjo, Bapak Ahmad Wahyudi, S. Hut, dimana dalam amanatnya beliau menyampaikan bahwa latihan kesamaptaan ini hendaknya dilakukan dengan serius oleh seluruh peserta karena latihan ini secara tidak langsung akan meningkatkan kedisiplinan, kekompakan dan kepatuhan kepada komando/perintah yang diberikan oleh pimpinan. Selain untuk kedisiplinan dan ketaatan, beliau menambahkan bahwa kesamaptaan juga untuk melatih ketangkasaan, membangun karakter dan kepercayaan diri sebagai tenaga pengamanan hutan.
                            </p>
                            <p class="blog-details__text">
                                Latihan yang diberikan instruktur dari Batalyon Infanteri Para Raider 503/Mayangkara Mojosari, Kabupaten Mojokerto untuk mencetak tenaga pengamanan yang handal adalah baris-berbaris, latihan fisik dan jasmani seperti sit-up, push-up, lari, senam.
