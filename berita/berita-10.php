                            <h3 class="blog-details__title">Hutan Lereng Arjuno di Jatiarjo Prigen Kembali Terbakar</h3>
                            <p class="blog-details__text">
                                PRIGEN, Radar Bromo–Kebakaran hutan kembali terjadi di wilayah Tahura R. Soerdjo. Kali ini giliran di lereng Gunung Arjuno, tepatnya di blok Sawahan Sesek, Desa Jatiarjo, Kecamatan Prigen, Kabupaten Pasuruan yang dilalap api.
                            </p>
                            <p class="blog-details__text">
                                Kebakaran hutan tersebut diketahui pertama kalinya pada Minggu (18/8) siang. Sesaat kemudian puluhan personel Pengamanan Hutan (Pamhut) Tahura R. Soerjo langsung naik menuju lokasi untuk melakukan pemadaman.
                            </p>
                            <p class="blog-details__text">
                               “Kebakaran hutannya masuk wilayah Pasuruan, tepatnya di lereng Gunung Arjuno di Desa Jatiarjo, Kecamatan Prigen. Titik api muncul Minggu (18/8) siang dan pemadamannya tuntas pada Senin (19/8) sore,” terang Agus Budi Utomo, kepala RPH 03 Tahura R. Soerdjo wilayah Pasuruan.
                            </p>
                            <p class="blog-details__text">
                                Dibandingkan kebakaran hutan sebelumnya, Upaya pemadamannya lebih mudah.
                            </p>
                            <p class="blog-details__text">
                                Hal itu baik yang berada di puncak Arjuno, wilayah Batu. Juga lereng Gunung Welirang, di Desa Lumbangrejo, Kecamatan Prigen, berbatasan dengan Trawas, Mojokerto beberapa pekan yang lalu.
                            </p>
                            <p class="blog-details__text">
                                Pemadaman cepat karena medannya berada di ketinggian sekitar 1.700 meter dari permukaan air laut. Dengan begitu, lebih terjangkau dan bentuknya padang savana. Sehingga, yang terbakar hanya berupa akasia, mlandingan, cemara gunung, alang–alang, dan semak belukar.
                            </p>
                            <p class="blog-details__text">
                                “Penyebab kebakaran masih belum bisa pastikan. Harus kami evaluasi dulu temuan-temuan didapati di lapangan. Untuk lahan yang terbakar luasnya sekitar 25 haktare,” tuturnya.
                            </p>
                            <p class="blog-details__text">
                                Bersamaan dengan kejadian kebakaran hutan tersebut, jalur pendakian baik ke Arjuno dan Welirang di lapangan ternyata masih buka atau beroperasi. “Jalur pendakiannya tak sampai ada penutupan sementara. Tetap dibuka untuk umum. Pertimbangannya, untuk lokasi kebakaran di luar jalur pendakian. Kami hanya mengimbau agar pendaki yang naik tetap hati-hati,” katanya. (zal/fun)
                            </p>
